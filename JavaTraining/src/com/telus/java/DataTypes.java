public class DataTypes {

	public static void main(String[] args) {
		
		//new updates
		
		//byte data type
		byte day = 7;
		System.out.println("Byte data type value is " + day);

		//short data type
		short atmPin = 2536;
		System.out.println("Short data type value is " + atmPin);

		//integer data type 
		int pinCode = 110022;
		System.out.println("Integer data type value is " + pinCode);

		//long data type
		long debitCardNumber = 002356453236l;
		System.out.println("Long data type value is " + debitCardNumber);

		//float data type
		float phi = 1.618033988749895f;
		System.out.println("Float data type value is " + phi);

		//double data type
		double phi2 = 1.618033988749895;
		System.out.println("Double data type value is " + phi2);

		//boolean data type
		boolean status = false;
		System.out.println("Boolean data type value is " + status);

		//character data type
		char firstLetter = 'K';
		System.out.println("Character data type value is " + firstLetter);

		//String data type
		String name = "Kamran Raza";
		System.out.println("String data type value is " + name);

	}

}
